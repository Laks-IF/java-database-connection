    private void jDeleteUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jDeleteUserMouseClicked
        String idToDelete = jIdToDeleteTextField.getText();
        
        try {
            usersDao.deleteUser(Integer.parseInt(idToDelete));
            
            loadUsers();
        } catch (Exception ex) {
            Logger.getLogger(ViewScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jDeleteUserMouseClicked

