    private void jUserActionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jUserActionMouseClicked
        int age = Integer.parseInt(jAgeTextField.getText());
        String username = jUsernameTextField.getText();
        String id = jIdTextField.getText();

        try {
            if (id.isEmpty()) {
                usersDao.createUser(new User(username, age), false);
            } else {
                usersDao.updateUser(Integer.parseInt(id), new User(username, age));
            }

            loadUsers();
        } catch (SQLException ex) {
            Logger.getLogger(ViewScreen.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(ViewScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jUserActionMouseClicked

