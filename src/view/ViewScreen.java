    private void defineConnection() {
        try {
            Connection connection = ConnectionFactory.getConnection(config);
            Statement statement = ConnectionFactory.getStatement(connection);

            System.out.println("Successful connected to MySQL Database");

            usersDao = new UsersDao(statement);

            System.out.println("Successful created UsersDao");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tentativa de acessar um usuário com ID inválido");
        }
    }

    private void loadUsers() {
        try {
            // On load window, load users
            users = usersDao.getAll();

            DefaultListModel<String> model = new DefaultListModel();

            for (int i = 0; i < users.size(); i++) {
                final User user = users.get(i);

                model.addElement(user.getName() + ", idade " + user.getAge() + ", ID = " + user.getId());
            }

            jUsersList.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(ViewScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

