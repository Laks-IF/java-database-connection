package shared;

import java.util.Random;

/**
 *
 * @author Novo
 */
public class Utils {

    public static int randomInt(int min, int max) {
        Random rand = new Random();

        int randomNum = rand.nextInt((max - min) + 1) + min;

        return randomNum;
    }
}
